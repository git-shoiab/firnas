public class Revision {
	//number type - byte, short, int, long
	//boolean type - true or false
	//floating type - float, double
	//char type - 'a'
	//String type - "aaa"
	
	public static void main(String[] args) {
		byte b=20;// 8 bit - value - -128 to 127
		b=-12;
		short s=300;//16 bit
		int n=10_0;//32 bit
		long l=433_453_4534_554L;//64 bit
		
		System.out.println(n);
		
		//For range values refer the ppt
		
		boolean boo=true;
		boo=false;
		
		float f=100;
		f=100.2f;
		double ddd=43445435345435534534545345345345344545343.5634324;
		
		System.out.println(ddd);
		
		char ch=97;
		System.out.println(ch);
		ch='a';		
		System.out.println(ch);
		
		String str="hello world";
		System.out.println(str);
	}
}
