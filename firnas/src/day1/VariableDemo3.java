package day1;

public class VariableDemo3 {
	public static void main(String[] args) {
		//Valid identifier or names
		
		int days;//valid
		int _days;//valid 
		int $days;//valid
		int _day2;//valid
		int $d_2ay;//valid
		
		// and more over the names in java has a OOA convention also
		
		//All variable names should start with lower case and next letter in upper case
		
		int distanceToMoon;//toggle case example
		
		//Note - class name should start with upper case
		//package names, method names, variable names should always start with lower case
		
		//INVALID NAMES
		
		//int 2days;//invalid - cannot start with a number
		//int %days;//not character other than _ or $ is used.
		
		//int da ys;//invalid - space cannot be used in between
		
		
	}
}
